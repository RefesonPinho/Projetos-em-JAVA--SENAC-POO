public class Cracha {
    
    String numero;
    String foto;
    String instituicao;
    String dataInicial;
    Aluno aluno;

    public Cracha(
        String numero,
        String foto,
        String instituicao,
        String dataInicial, 
        Aluno aluno
    ) {
        this.numero = numero;
        this.foto = foto;
        this.instituicao = instituicao;
        this.dataInicial = dataInicial;
        this.aluno = aluno;
    }
}
